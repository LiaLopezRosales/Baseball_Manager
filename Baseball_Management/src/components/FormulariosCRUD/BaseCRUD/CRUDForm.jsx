import React from "react";

const CRUDForm = ({
    fields,
    formValues,
    formErrors,
    onChange,
    onSave,
    onCancel,
}) => {

    return (
        <div className="item-form">
            <h2>{formValues.id ? "Editar" : "Crear"}</h2>
            {fields
                .filter((field) => !field.autoGenerated)
                .map((field) => (
                    <div className="form-group" key={field.name}>
                        <label>{field.label}:</label>
                        {/* <input
                            type={field.type === "password" && field.showPassword ? "text" : field.type}
                            name={field.name}
                            value={formValues[field.name] || ""}
                            onChange={onChange}
                        />
                        {field.type === "password" && field.toggleVisibility && (
                            <button type="button" onClick={field.toggleVisibility}>
                                {field.showPassword ? "Ocultar" : "Mostrar"}
                            </button>
                        )} */}

                        {field.type === "password" ? (
                            <div className="password-container">
                                <input
                                    type={field.showPassword ? "text" : "password"}
                                    name={field.name}
                                    value={formValues[field.name] || ""}
                                    onChange={onChange}
                                />
                                {field.toggleVisibility && (
                                    <button
                                        type="button"
                                        onClick={field.toggleVisibility}
                                        className="toggle-password"
                                    >
                                        {field.showPassword ? "Ocultar" : "Mostrar"}
                                    </button>
                                )}
                            </div>
                        ) : field.type === "select" ? (
                            <select
                                name={field.name}
                                value={formValues[field.name] || ""}
                                onChange={onChange}
                            >
                                <option value="">Seleccione una opci√≥n</option>
                                {field.options.map((option) => (
                                    <option key={option.id} value={option.id}>
                                        {option.name}
                                    </option>
                                ))}
                            </select>
                        ) : (
                            <input
                                type={field.type}
                                name={field.name}
                                value={formValues[field.name] || ""}
                                onChange={onChange}
                            />
                        )}


                        {formErrors[field.name] && (
                            <div className="error-message">{formErrors[field.name][0]}</div>
                        )}
                    </div>
                ))}
            <div className="form-actions">
                <button onClick={onSave}>Guardar</button>
                <button onClick={onCancel}>Cancelar</button>
            </div>
        </div>
    );
};

export default CRUDForm;




